    glPopMatrix();
    
    glColor3ub(0,0,255);   
    glTranslatef(0, 600, 0);
    glutWireTorus(100.0, 200.0, 50,50); //线框圆环
    
    glColor3ub(255,69,0);
    glTranslatef(0, -1200, 0);
    glutSolidCube(250);     //实心立方体//绘制部分
    
    glColor3ub(255,140,0);
    glTranslatef(700, 600, 0);
    glutSolidTorus(80, 160, 150, 150); //实心圆环
    
